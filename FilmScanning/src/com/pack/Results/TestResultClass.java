package com.pack.Results;

public class TestResultClass {
	
	private String TestCaseID;
	private String TestStepID;
	private String TestCaseDesc;
	private String TestStepDesc;
	private String TestResult;
	private String TestResultException;
	
	

	/**
	 * @return the testCaseID
	 */
	public String getTestCaseID() {
		return TestCaseID;
	}

	/**
	 * @param testCaseID the testCaseID to set
	 */
	public void setTestCaseID(String testCaseID) {
		TestCaseID = testCaseID;
	}

	/**
	 * @return the testStepID
	 */
	public String getTestStepID() {
		return TestStepID;
	}

	/**
	 * @param testStepID the testStepID to set
	 */
	public void setTestStepID(String testStepID) {
		TestStepID = testStepID;
	}

	/**
	 * @return the testCaseDesc
	 */
	public String getTestCaseDesc() {
		return TestCaseDesc;
	}

	/**
	 * @param testCaseDesc the testCaseDesc to set
	 */
	public void setTestCaseDesc(String testCaseDesc) {
		TestCaseDesc = testCaseDesc;
	}

	/**
	 * @return the testStepDesc
	 */
	public String getTestStepDesc() {
		return TestStepDesc;
	}

	/**
	 * @param testStepDesc the testStepDesc to set
	 */
	public void setTestStepDesc(String testStepDesc) {
		TestStepDesc = testStepDesc;
	}

	/**
	 * @return the testResult
	 */
	public String getTestResult() {
		return TestResult;
	}

	/**
	 * @param testResult the testResult to set
	 */
	public void setTestResult(String testResult) {
		TestResult = testResult;
	}

	/**
	 * @return the testResultException
	 */
	public String getTestResultException() {
		return TestResultException;
	}

	/**
	 * @param testResultException the testResultException to set
	 */
	public void setTestResultException(String testResultException) {
		TestResultException = testResultException;
	}
	

}
